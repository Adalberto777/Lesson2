void PrintPic (int[,] pic1)
{
    for (int i = 0; i < pic1.GetLength(0); i++)  // matrix.GetLength(0) конструкция выдает количество строк или столбцов 
    {                                              // в зависимости 0 или 1 (0- строки, 1 столбцы)
        for (int j = 0; j < pic1.GetLength(1); j++)
    {
        if (pic1[i, j] == 0) Console.Write(" ");
        else Console.Write("+");
    }
    Console.WriteLine();
    }
}

void PaintPourPic(int line, int colomn)
{
